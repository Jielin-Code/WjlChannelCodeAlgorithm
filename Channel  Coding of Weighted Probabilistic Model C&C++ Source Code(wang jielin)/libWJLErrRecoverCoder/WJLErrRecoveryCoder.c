//--------------------------------------------------------------------------------------------------------------------------
unsigned char *WJL_ErrRecovery_Encode_Buff(unsigned char *ucInBuffer,const unsigned int *unInbufferLen,unsigned int *unOutbufferLen)
{
	WJL_ERRRECOVERY_ENCODER erEncoder;
	unsigned char ucTail[16] = {0xcf,0xbd,0xcf,0xfa,0x03,0xb9,0x5c,0x08,0x15,0xd1,0x34,0x03,0xfe,0x69,0xc2,0x60};

	unsigned int unInbuffLenTmp = *unInbufferLen + 16;
	unsigned char *ucInBuffTmp = (unsigned char*)malloc(unInbuffLenTmp);
	if (NULL == ucInBuffTmp)
	{
		return NULL;
	}
	memcpy(ucInBuffTmp,ucInBuffer,*unInbufferLen);
	memcpy(ucInBuffTmp+*unInbufferLen,ucTail,16);

	Encode_Init(&erEncoder,ucInBuffTmp,&unInbuffLenTmp);
	Encode_Agent(&erEncoder,ucInBuffTmp,&unInbuffLenTmp);
	free(ucInBuffTmp);
	Encode_End(&erEncoder);

	*unOutbufferLen = erEncoder.out_buff_size;
	return erEncoder.out_buff;

}

//--------------------------------------------------------------------------------------------------------------------------
unsigned char *WJL_ErrRecovery_Decode_Buff(unsigned char *ucInBuffer,const unsigned int *unInbufferLen,unsigned int *unOutbufferLen,unsigned  int *unPosErr)
{
	WJL_ERRRECOVERY_DECODER erDecoder;

	ErrRecovery_Init(&erDecoder,ucInBuffer,unInbufferLen);
	if (!ErrRecovery_Agent(&erDecoder,unInbufferLen,unPosErr))
	{
		*unOutbufferLen = erDecoder.out_buff_size;
		Decode_Init(&erDecoder,unOutbufferLen);
		Decode_Agent(&erDecoder,unOutbufferLen);
	}

	*unOutbufferLen -= 16;
	erDecoder.out_buff = (unsigned char*)realloc(erDecoder.out_buff,*unOutbufferLen);
	return erDecoder.out_buff;
}
